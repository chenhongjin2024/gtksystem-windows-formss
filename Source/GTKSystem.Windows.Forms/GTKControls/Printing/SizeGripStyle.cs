namespace System.Windows.Forms
{
    public enum SizeGripStyle
    {
        /// <summary>
        ///  The size grip is automatically display when needed.
        /// </summary>
        Auto = 0,

        /// <summary>
        ///  The sizing grip is always shown on the form.
        /// </summary>
        Show = 1,

        /// <summary>
        ///  The sizing grip is hidden.
        /// </summary>
        Hide = 2,
    }
}
